export default {
  async fetch(request, env) {
    const corsHeaders = getCorsHeaders(request);

    if (request.method === "OPTIONS") {
      return new Response(null, { headers: corsHeaders });
    }

    if (request.method !== "POST") {
      return new Response("Method not allowed", {
        status: 405,
        headers: corsHeaders,
      });
    }

    try {
      const { text, now, localNow, timezone, mode } = await request.json();

      if (!text || typeof text !== "string") {
        return jsonResponse({ error: "Text is required" }, 400, corsHeaders);
      }

      const systemPrompt = mode === "search"
        ? getSearchSystemPrompt(now, localNow, timezone)
        : getReminderSystemPrompt(now, localNow, timezone);

      const aiResponse = await fetch("https://api.anthropic.com/v1/messages", {
        method: "POST",
        headers: {
          "Content-Type": "application/json; charset=utf-8",
          "x-api-key": env.ANTHROPIC_API_KEY,
          "anthropic-version": "2023-06-01",
        },
        body: JSON.stringify({
          model: "claude-haiku-4-5-20251001",
          max_tokens: 300,
          temperature: 0,
          system: systemPrompt,
          messages: [
            {
              role: "user",
              content: text,
            },
          ],
        }),
      });

      if (!aiResponse.ok) {
        const errorText = await aiResponse.text();

        return jsonResponse(
          {
            error: "AI request failed",
            status: aiResponse.status,
            details: errorText,
          },
          502,
          corsHeaders,
        );
      }

      const aiData = await aiResponse.json();
      const answerText = aiData.content?.[0]?.text?.trim() || "";
      const cleanText = answerText.replace(/```json|```/g, "").trim();

      if (cleanText === "null") {
        return jsonResponse(null, 200, corsHeaders);
      }

      return new Response(cleanText, {
        headers: {
          ...corsHeaders,
          "Content-Type": "application/json; charset=utf-8",
        },
      });
    } catch (error) {
      return jsonResponse({ error: "Worker error" }, 500, corsHeaders);
    }
  },
};

